package operators;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 15;

        System.out.println("Arithmetic operation- Addition: " + (num1 + num2));
        System.out.println("Arithmetic operation- Subtraction: " + (num1 - num2));
        System.out.println("Arithmetic operation- Multiplication: " + (num1 * num2));
        System.out.println("Arithmetic operation- Division: " + (num2 / num1));
        System.out.println("Arithmetic operation- Modulo: " + (num2 % num1)); // if numerator is less then result will be numerator only
    }
}
